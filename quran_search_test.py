def test_search_response_format():
    """Test that search results have proper format with verse text, sura names, and verse numbers"""
    print("\nüîç Testing Search Response Format and Data Structure...")
    try:
        response = requests.get(f"{BASE_URL}/quran/search", params={"query": "ÿßŸÑŸÑŸá"})
        print(f"   Status Code: {response.status_code}")
        
        if response.status_code == 200:
            data = response.json()
            results = data.get("results", [])
            
            if len(results) > 0:
                # Test first few results for proper structure
                for i, result in enumerate(results[:3]):
                    print(f"   Testing result {i+1} structure...")
                    
                    # Check required fields
                    required_fields = ["surahNumber", "nameAr", "nameEn", "ayah", "textAr"]
                    missing_fields = [field for field in required_fields if field not in result]
                    
                    if missing_fields:
                        print(f"   ‚ùå FAIL: Missing fields in result {i+1}: {missing_fields}")
                        return False
                    
                    # Verify data types and content
                    if not isinstance(result["surahNumber"], int) or result["surahNumber"] < 1:
                        print(f"   ‚ùå FAIL: Invalid surahNumber in result {i+1}: {result['surahNumber']}")
                        return False
                    
                    if not isinstance(result["ayah"], int) or result["ayah"] < 1:
                        print(f"   ‚ùå FAIL: Invalid ayah number in result {i+1}: {result['ayah']}")
                        return False
                    
                    if not result["nameAr"] or not result["nameEn"]:
                        print(f"   ‚ùå FAIL: Empty sura names in result {i+1}")
                        return False
                    
                    if not result["textAr"] or "ÿßŸÑŸÑŸá" not in result["textAr"]:
                        print(f"   ‚ùå FAIL: Invalid or missing Arabic text in result {i+1}")
                        return False
                    
                    print(f"   ‚úÖ Result {i+1}: Surah {result['surahNumber']} ({result['nameAr']} / {result['nameEn']}), Ayah {result['ayah']}")
                
                print(f"   ‚úÖ PASS: All tested results have proper format with verse text, sura names, and verse numbers")
                return True
            else:
                print("   ‚ùå FAIL: No results to test format")
                return False
        else:
            print(f"   ‚ùå FAIL: Expected status 200, got {response.status_code}")
            return False
    except Exception as e:
        print(f"   ‚ùå ERROR: {str(e)}")
        return False

